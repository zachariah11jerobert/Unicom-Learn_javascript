// Arithmetic Operators In Javascript
let num1 = 95;
let num2 = 3;

let answer;
console.log(answer);
answer = num1 + num2;
console.log("Addition Answer is : " + answer);

answer = num1 - num2;
console.log("Substraction Answer is : " + answer);

answer = num1 * num2;
console.log("Multiplication Answer is : " + answer);

answer = num1 / num2;
console.log("Division Answer is : " + answer);

answer = num1 % num2;
console.log("Modulus Answer is : " + answer);